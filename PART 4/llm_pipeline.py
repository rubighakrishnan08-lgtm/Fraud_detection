# REUSABLE LLM FUNCTION
def call_llm(system_prompt,
             user_prompt,
             temperature=0.0,
             max_tokens=512):
    cache_file = "llm_cache.json"
    if os.path.exists(cache_file):
        with open(cache_file, "r") as f:
            cache = json.load(f)
    else:
        cache = {}

    key = f"{hash(user_prompt)}_{temperature}"

    if key in cache:
        print("Loaded response from cache.")
        return json.dumps(cache[key], indent=4)
    prompt = f"""
System:
{system_prompt}

User:
{user_prompt}
"""
    try:
        response = client.models.generate_content(
            model=MODEL_NAME,
            contents=prompt,
            config=types.GenerateContentConfig(
                temperature=temperature,
                max_output_tokens=max_tokens))
        text = response.text
        try:
            cache[key] = json.loads(text)
        except:
            cache[key] = text
        with open(cache_file, "w") as f:
            json.dump(cache, f, indent=4)
        return text
    except Exception as e:
        print("\nGemini API Error")
        print(e)
        return None
    
