# JSON VALIDATION FUNCTION
def validate_llm_json(response_text):
    """
    Validate the JSON returned by the LLM.
    Steps:
    1. Remove extra whitespace.
    2. Parse JSON.
    3. Validate against OUTPUT_SCHEMA.
    4. Return fallback dictionary if anything fails.
    """
    # Handle Empty Response
    if response_text is None:
        print("\nNo response received from LLM.")
        return FALLBACK_RESPONSE
    # Remove leading/trailing whitespace
    response_text = response_text.strip()

    # Parse JSON
    try:
        parsed_json = json.loads(response_text)
        print("\nJSON Parsing Successful.")
    except json.JSONDecodeError as error:
        print("\nJSON Parsing Failed")
        print(error)
        return FALLBACK_RESPONSE
    if "confidence_level" in parsed_json:
        parsed_json["confidence_level"] = parsed_json["confidence_level"].lower()
    if "prediction_label" in parsed_json and isinstance(parsed_json["prediction_label"], str):
        label = parsed_json["prediction_label"].strip().lower()
    if label == "fraud":
        parsed_json["prediction_label"] = "Fraud"
    elif label in ["not fraud", "not_fraud"]:
        parsed_json["prediction_label"] = "Not Fraud"

    # Validate JSON Schema
    try:
        validate(
            instance=parsed_json,
            schema=OUTPUT_SCHEMA)
        print("Schema Validation : PASSED")
        return parsed_json
    except ValidationError as error:
        print("\nSchema Validation Failed")
        print(error.message)
        return FALLBACK_RESPONSE

# PII GUARDRAIL
def has_pii(text):
    """
    Detect whether the supplied text contains
    an email address or phone number.
    Returns
    bool
    """
    email_pattern = (r"[a-zA-Z0-9_.+-]+"r"@[a-zA-Z0-9-]+"r"\.[a-zA-Z0-9-.]+")
    phone_pattern = (r"\b\d{10}\b"r"|"r"\b\d{3}[-.\s]\d{3}[-.\s]\d{4}\b")
    return bool(
        re.search(email_pattern, text) 
        or
        re.search(phone_pattern, text))

# SAFE LLM WRAPPER
def safe_call_llm(
        call_function,
        system_prompt,
        user_prompt,
        temperature=0.0,
        max_tokens=512):
    """
    Execute the LLM call only if the user input
    does not contain PII.
    """
    if has_pii(user_prompt):
        print("Input blocked: PII detected.")
        return None
    print("No PII detected.")
    print("Sending request to LLM...")
    response = call_function(
        system_prompt=system_prompt,
        user_prompt=user_prompt,
        temperature=temperature,
        max_tokens=max_tokens)
    return response

# FORMAT USER PROMPT
